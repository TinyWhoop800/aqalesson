import json  # Модуль для работы с JSON файлами (парсинг и сериализация)
import sys  # Модуль для работы с системными функциями (stderr, stdout, exit)
import argparse  # Модуль для обработки аргументов командной строки


# ============================================================
# ФУНКЦИЯ ПАРСИНГА PYTEST JSON ОТЧЕТА
# ============================================================
def parse_pytest_json(json_file_path):
    """
    Парсит JSON отчет pytest и извлекает статистику тестирования.

    Args:
        json_file_path (str): Путь к JSON файлу с результатами pytest

    Returns:
        dict: Словарь со статистикой тестов или None при ошибке
    """

    # Блок try-except для обработки ошибок при чтении файла
    try:
        # Открываем JSON файл для чтения с явным указанием кодировки UTF-8
        # 'with' автоматически закроет файл после завершения блока
        with open(json_file_path, 'r', encoding='utf-8') as f:
            # Парсим JSON содержимое в Python словарь
            data = json.load(f)
    except FileNotFoundError:
        # Обрабатываем случай, когда файл не найден
        # file=sys.stderr перенаправляет вывод в поток ошибок (stderr)
        print(f"Error: JSON file not found: {json_file_path}", file=sys.stderr)
        return None
    except json.JSONDecodeError as e:
        # Обрабатываем случай некорректного JSON формата
        print(f"Error: Invalid JSON format: {e}", file=sys.stderr)
        return None

    # ============================================================
    # ИЗВЛЕЧЕНИЕ ДАННЫХ ИЗ JSON СТРУКТУРЫ
    # ============================================================

    # Получаем секцию 'summary' из JSON отчета pytest
    # .get() используется для безопасного доступа (возвращает {} если ключ отсутствует)
    summary = data.get('summary', {})

    # Извлекаем количество тестов по категориям
    # Второй параметр 0 - значение по умолчанию, если ключ отсутствует
    total_tests = summary.get('total', 0)  # Общее количество тестов
    passed_tests = summary.get('passed', 0)  # Количество успешных тестов
    failed_tests = summary.get('failed', 0)  # Количество упавших тестов
    skipped_tests = summary.get('skipped', 0)  # Количество пропущенных тестов
    error_tests = summary.get('error', 0)  # Количество тестов с ошибками

    # ============================================================
    # РАСЧЕТ ПРОЦЕНТА УСПЕШНОСТИ
    # ============================================================

    # Вычисляем процент успешных тестов
    if total_tests > 0:
        # Формула: (пройденные / всего) * 100
        # round() округляет до 2 знаков после запятой
        success_rate = round((passed_tests / total_tests) * 100, 2)
    else:
        # Если тестов нет, устанавливаем success_rate в 0
        success_rate = 0.0

    # ============================================================
    # ОБРАБОТКА ДЛИТЕЛЬНОСТИ ВЫПОЛНЕНИЯ
    # ============================================================

    # Извлекаем общую длительность выполнения в секундах
    duration_seconds = data.get('duration', 0)

    # Форматируем длительность в человекочитаемый формат (1h 30m / 5m 20s / 45.5s)
    duration_formatted = format_duration(duration_seconds)

    # ============================================================
    # ВОЗВРАТ РЕЗУЛЬТАТОВ
    # ============================================================

    # Возвращаем словарь со всей статистикой
    return {
        'total': total_tests,  # Общее количество
        'passed': passed_tests,  # Пройденные
        'failed': failed_tests,  # Упавшие
        'skipped': skipped_tests,  # Пропущенные
        'error': error_tests,  # С ошибками
        'success_rate': success_rate,  # Процент успешности
        'duration_seconds': duration_seconds,  # Длительность в секундах (raw)
        'duration_formatted': duration_formatted  # Форматированная длительность
    }


# ============================================================
# ФУНКЦИЯ ФОРМАТИРОВАНИЯ ДЛИТЕЛЬНОСТИ
# ============================================================
def format_duration(seconds):
    """
    Форматирует длительность в секундах в человекочитаемый формат.

    Args:
        seconds (float): Длительность в секундах

    Returns:
        str: Форматированная строка (например: "1h 30m", "5m 20s", "45.5s")
    """

    # Если меньше минуты (< 60 секунд)
    if seconds < 60:
        # Форматируем как секунды с одним знаком после запятой
        return f"{seconds:.1f}s"

    # Если меньше часа (< 3600 секунд = 60 минут)
    elif seconds < 3600:
        # Вычисляем целые минуты (оператор // - целочисленное деление)
        minutes = int(seconds // 60)
        # Вычисляем оставшиеся секунды (оператор % - остаток от деления)
        remaining_seconds = int(seconds % 60)
        # Форматируем как "Xm Ys"
        return f"{minutes}m {remaining_seconds}s"

    # Если час и больше
    else:
        # Вычисляем целые часы
        hours = int(seconds // 3600)
        # Вычисляем оставшиеся минуты после вычета часов
        minutes = int((seconds % 3600) // 60)
        # Форматируем как "Xh Ym"
        return f"{hours}h {minutes}m"


# ============================================================
# ФУНКЦИЯ ВЫВОДА СТАТИСТИКИ В РАЗЛИЧНЫХ ФОРМАТАХ
# ============================================================
def output_stats(stats, output_format='json'):
    """
    Выводит статистику тестов в указанном формате.

    Args:
        stats (dict): Словарь со статистикой тестов
        output_format (str): Формат вывода ('json', 'env', 'text')
    """

    # Проверка наличия данных
    if not stats:
        return

    # ============================================================
    # ФОРМАТ JSON - для программной обработки другими инструментами
    # ============================================================
    if output_format == 'json':
        # Сериализуем словарь в JSON с отступами для читаемости
        # indent=2 делает JSON красиво отформатированным
        print(json.dumps(stats, indent=2))

    # ============================================================
    # ФОРМАТ ENV - для использования в shell скриптах и GitLab CI
    # ============================================================
    elif output_format == 'env':
        # Выводим в формате KEY=VALUE для экспорта в переменные окружения
        # Такой формат можно использовать через: source test_stats.env
        print(f"TOTAL_TESTS={stats['total']}")
        print(f"PASSED_TESTS={stats['passed']}")
        print(f"FAILED_TESTS={stats['failed']}")
        print(f"SKIPPED_TESTS={stats['skipped']}")
        print(f"ERROR_TESTS={stats['error']}")
        print(f"SUCCESS_RATE={stats['success_rate']}")
        # Кавычки вокруг duration нужны для корректной работы со строками в shell
        print(f"TEST_DURATION='{stats['duration_formatted']}'")

    # ============================================================
    # ФОРМАТ TEXT - человекочитаемый вывод для консоли
    # ============================================================
    elif output_format == 'text':
        # Выводим красивый отформатированный текстовый отчет
        print(f"Test Results Summary:")
        print(f"  Total Tests: {stats['total']}")
        print(f"  Passed: {stats['passed']}")
        print(f"  Failed: {stats['failed']}")
        print(f"  Skipped: {stats['skipped']}")
        print(f"  Errors: {stats['error']}")
        print(f"  Success Rate: {stats['success_rate']}%")
        print(f"  Duration: {stats['duration_formatted']}")


# ============================================================
# ГЛАВНАЯ ФУНКЦИЯ - ТОЧКА ВХОДА В ПРОГРАММУ
# ============================================================
def main():
    """
    Главная функция, которая обрабатывает аргументы командной строки
    и координирует работу всех других функций.
    """

    # ============================================================
    # СОЗДАНИЕ ПАРСЕРА АРГУМЕНТОВ КОМАНДНОЙ СТРОКИ
    # ============================================================

    # ArgumentParser - класс для обработки аргументов CLI
    parser = argparse.ArgumentParser(
        description='Extract test statistics from pytest JSON output'
    )

    # ============================================================
    # ОПРЕДЕЛЕНИЕ ПОЗИЦИОННОГО АРГУМЕНТА (ОБЯЗАТЕЛЬНЫЙ)
    # ============================================================

    # Добавляем обязательный позиционный аргумент - путь к JSON файлу
    # Позиционные аргументы не имеют префиксов типа -- или -
    parser.add_argument(
        'json_file',  # Имя аргумента
        help='Path to pytest JSON output file'  # Описание для --help
    )

    # ============================================================
    # ОПРЕДЕЛЕНИЕ ОПЦИОНАЛЬНЫХ АРГУМЕНТОВ (С ФЛАГАМИ)
    # ============================================================

    # Аргумент --format для выбора формата вывода
    parser.add_argument(
        '--format',  # Длинный флаг
        choices=['json', 'env', 'text'],  # Ограничиваем выбор этими опциями
        default='json',  # Значение по умолчанию
        help='Output format (default: json)'  # Описание для --help
    )

    # Аргумент --output для указания файла вывода (опциональный)
    parser.add_argument(
        '--output',  # Флаг
        help='Output file path (default: stdout)'  # Описание для --help
    )

    # ============================================================
    # ПАРСИНГ АРГУМЕНТОВ
    # ============================================================

    # parse_args() обрабатывает sys.argv и возвращает объект Namespace
    # Все аргументы доступны как атрибуты: args.json_file, args.format, args.output
    args = parser.parse_args()

    # ============================================================
    # ОБРАБОТКА JSON ФАЙЛА
    # ============================================================

    # Вызываем функцию парсинга JSON
    stats = parse_pytest_json(args.json_file)

    # Если парсинг завершился с ошибкой (вернулся None)
    if stats is None:
        # Выходим из программы с кодом ошибки 1 (стандарт для ошибок)
        sys.exit(1)

    # ============================================================
    # ВЫВОД РЕЗУЛЬТАТОВ
    # ============================================================

    # Если указан файл для вывода через --output
    if args.output:
        # Сохраняем оригинальный stdout для восстановления позже
        original_stdout = sys.stdout

        # Блок try-finally гарантирует восстановление stdout даже при ошибках
        try:
            # Открываем выходной файл для записи
            with open(args.output, 'w', encoding='utf-8') as f:
                # Перенаправляем stdout в файл
                # Теперь все print() будут записываться в файл
                sys.stdout = f
                # Выводим статистику (попадет в файл)
                output_stats(stats, args.format)
        finally:
            # Восстанавливаем оригинальный stdout в любом случае
            sys.stdout = original_stdout

        # Информируем пользователя о записи в файл (вывод в консоль)
        print(f"Statistics written to: {args.output}")

    # Если файл не указан - выводим в stdout (консоль)
    else:
        output_stats(stats, args.format)


# ============================================================
# ТОЧКА ВХОДА СКРИПТА
# ============================================================

# Это стандартная Python конструкция для точки входа
# Код выполнится только при прямом запуске скрипта
# Не выполнится, если скрипт импортирован как модуль
if __name__ == '__main__':
    main()